// SEOPlugin owns /robots.txt generation and the SEO/robots admin
// settings schemas. The schemas are declared in extension.json and
// registered via the kernel's settings bridge automatically; this
// plugin only needs to implement the public route.
//
// The robots.txt logic was historically in internal/cms/robots_handler.go.
// Per the kernel/extensions hard rule (CLAUDE.md), SEO is a feature, not
// kernel infrastructure — disabling this extension cleanly removes
// /robots.txt without leaving dead code in the kernel.
type SEOPlugin struct {
	host *coreapi.GRPCHostClient
}

func (p *SEOPlugin) GetSubscriptions() ([]*pb.Subscription, error) {
	// "render.head" is the kernel's hook for injecting <head> content
	// on every public render. The kernel passes the full template-facing
	// node payload, settings, and translations — extensions extract
	// whatever they care about. This extension renders SEO/OG/Twitter
	// tags from the per-node `seo` JSONB and site settings; an analytics
	// or verification extension subscribing to the same event would add
	// its own tags alongside.
	return []*pb.Subscription{
		{EventName: "render.head", Priority: 50},
	}, nil
}

func (p *SEOPlugin) HandleEvent(action string, payload []byte) (*pb.EventResponse, error) {
	switch action {
	case "render.head":
		return p.handleRenderHead(payload)
	}
	return &pb.EventResponse{Handled: false}, nil
}

func (p *SEOPlugin) Shutdown() error { return nil }

func (p *SEOPlugin) Initialize(hostConn *grpc.ClientConn) error {
	p.host = coreapi.NewGRPCHostClient(coreapipb.NewSquillaHostClient(hostConn))
	return nil
}

func (p *SEOPlugin) HandleHTTPRequest(req *pb.PluginHTTPRequest) (*pb.PluginHTTPResponse, error) {
	method := strings.ToUpper(req.GetMethod())
	path := req.GetPath()

	if method == "GET" && (path == "/robots.txt" || strings.HasSuffix(path, "/robots.txt")) {
		return p.serveRobots(context.Background(), req)
	}

	return &pb.PluginHTTPResponse{
		StatusCode: 404,
		Headers:    map[string]string{"Content-Type": "text/plain; charset=utf-8"},
		Body:       []byte("not found"),
	}, nil
}

